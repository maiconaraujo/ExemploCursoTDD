package modelo;

/**
 *
 * @author MAICON
 */
public class Usuario {
    private int codigo;
    private String nome;

    public Usuario() {
    }

    
    public Usuario(String nome) {
        this.codigo = 0;
        this.nome = nome;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
     
    
}
